import Foundation
import UIKit

extension UIColor
{
    class func backgroundColor() -> UIColor
    {
        return UIColor(red: 239/255, green: 250/255, blue: 200/255, alpha:1.0)
    }
    
    class func selectorColor() -> UIColor
    {
        return UIColor(red: 64/255, green: 171/255, blue: 200/255, alpha:1.0)
    }
}
